    private int Autentificar(String user, String pass) {
        //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        for (int i = 0; i < Variables.Usuarios.size(); i++) {
            Usuario u = Variables.Usuarios.get(i);
            if(u.getUser().equals(user) && u.getPassword().equals(pass)){
                Variables.usuarioLoggeado = u;
                this.hide();
                if(!u.isAdmin()){
                    InicioPartida ii = new InicioPartida();
                    ii.setVisible(true);
                    ii.show();
                    ii.setLocationRelativeTo(null);
                    return 1;
                }
                else{
                    VistaSontay ii = new VistaSontay();
                    ii.setVisible(true);
                    ii.show();
                    ii.setLocationRelativeTo(null);
                    return 1;
                }
            }
        }
        JOptionPane.showMessageDialog(this, "USUARIO NO ENCONTRADO");
        return 0;
    }

