    private boolean ExisteUsuario(String user) {
        for (int i = 0; i < Variables.Usuarios.size(); i++) {
            Usuario u = Variables.Usuarios.get(i);
            if(u.getUser().equals(user)){
                return true;
            }
        }
        return false;
    }
