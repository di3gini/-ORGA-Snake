    private void btn_crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_crearActionPerformed
        // TODO add your handling code here:
        char[] arrayC = txt_pass.getPassword(); 
        String pass = new String(arrayC);
        
        char[] arrayB = txt_confirmpass.getPassword(); 
        String confirm = new String(arrayB);
        
        if(pass.equals(confirm)){
            if(!txt_user.getText().equals("") && !ExisteUsuario(txt_user.getText())){
                Usuario u = new Usuario();
                u.setNombre(txt_nombre.getText());
                u.setPassword(pass);
                u.setUser(txt_user.getText());
                Variables.Usuarios.add(u);
                
                JOptionPane.showMessageDialog(this, "USUARIO CREADO CON EXITO");
            }
            else{
                JOptionPane.showMessageDialog(this, "EL USUARIO YA EXISTE O NO ES VALIDO");
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "LAS CONTRASEÃ‘AS NO COINCIDEN");
        }
    }//GEN-LAST:event_btn_crearActionPerformed

