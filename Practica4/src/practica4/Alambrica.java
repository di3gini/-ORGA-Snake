    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        System.out.println("Puertos Disponibles");
        Enumeration ports = CommPortIdentifier.getPortIdentifiers();
        timer = new Thread(new Alambrica.ImplementoRunnable());
        timer.start();
        timer.suspend();
        while (ports.hasMoreElements()) {
            puerto = (CommPortIdentifier) ports.nextElement();
            if (puerto.getName().equals("COM4")) {
                System.out.println(puerto.getName());

                try {
                    // TODO add your handling code here:
                    puerto_serial = (SerialPort) puerto.open("puerto serial", 2000);
                    System.out.println("Se establecio la conexion");
                    puerto_serial.setSerialPortParams(75, SerialPort.DATABITS_8, SerialPort.STOPBITS_1, SerialPort.PARITY_NONE);
                    in = new Scanner(puerto_serial.getInputStream());
                    //System.out.println();
                    timer.resume();

                } catch (PortInUseException | IOException | UnsupportedCommOperationException ex) {
                    Logger.getLogger(Alambrica.class.getName()).log(Level.SEVERE, null, ex);
                    System.out.println("Error");
                }
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

