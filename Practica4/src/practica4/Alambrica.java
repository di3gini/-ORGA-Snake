    private char returnChar(String val) {
        System.out.println("valor string: " + val);
        int decimalValue = Integer.parseInt(val, 2);//Convertimos el valor de la cadenas en un valor binario

        System.out.println("Valor decimal: " + decimalValue);
        char valor = (char) decimalValue;//El valor binario lo convertimos en un valor char 
        System.out.println("Valor Char: " + valor);
        return valor;//retornamos el caracter
    }
